#include<iostream>
using namespace std;

int main()
{
	cout<<"The size of the integer datatype is :: "<<sizeof(int);
	cout<<"\n \n";
	
	cout<<"The size of the float datatype is :: "<<sizeof(float);
	cout<<"\n \n";
	
	cout<<"The size of the character datatype is :: "<<sizeof(char);
	cout<<"\n \n";
	
	cout<<"The size of the double datatype is :: "<<sizeof(double);
	cout<<"\n \n";
	
	return 0;
}
